public class FundicaoDeTipoJava {
	public static void main(String[] args) {
		int numero0 = 1234;
		float numero1 = numero0;
		double numero2 = numero0;
		long numero3S = numero0;
		System.out.println(numero0+" isso é um inteira.");
		System.out.println(numero1+" isso é um float.");
		System.out.println(numero2+" isso é um double.");
		System.out.println(numero3S+" isso é uma long.");
	}
}
